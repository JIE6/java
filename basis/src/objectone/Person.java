package objectone;

/**
 * 重点
 * 定义类-定义
 *
 * 下面的Person 类代码里没有定义构造器，系统将为它提供一个默认的构造器。系统提供的构造器总是无参数的。
 * @author JIE
 */
public class Person {

    // 下面定义了两个成员变量

    public String name;
    public int age;

    // 下面定义了一个say方法

    public void say(String content) {
        System.out.println(content);
    }


}
